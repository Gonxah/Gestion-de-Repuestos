class Repuesto {
    constructor(codigo, nombre, descripcion, marca, modelo, precio, categoria, disponibilidad, stock, ubicacion, estado, tipo, ) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.descripcion = descripcion;
        this.marca = marca;
        this.modelo = modelo;
        this.precio = precio;
        this.categoria = categoria;
        this.disponibilidad = disponibilidad;
        this.stock = stock;
        this.ubicacion = ubicacion;
        this.estado = estado;
        this.tipo = tipo;
    }
}
